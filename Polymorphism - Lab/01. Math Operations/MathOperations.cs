using System;
using System.Collections.Generic;
using System.Text;

namespace Operations
{
    public class MathOperations
    {
        public int Add(int x,int y)
        {
            return x + y;
        }
        public double Add(double a, double b,double c)
        {
            return a + b + c;
        }
        public decimal Add(decimal z, decimal q,decimal v)
        {
            return z + q + v;
        }
    }
}
